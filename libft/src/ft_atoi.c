int	ft_atoi(const char	*str)
{
	int	i;
	int	sign;
	int	put;

	i = 0;
	sign = 1;
	put = 0;
	while (str[i] && ((str[i] >= 9 && str[i] <= 13) || str[i] == 32))
		i++;
	if (str[i] == '-')
	{
		sign = -1;
		i++;
	}
	else if (str[i] == '+')
	{
		sign = 1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		put = put * 10 + (str[i] - '0');
		i++;
	}
	return (put * sign);
}
